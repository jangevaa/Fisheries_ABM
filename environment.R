###########################################################
# SCOPE:
# This file will define the environment in which the
# simulated fish move through their life cycle in space and
# time. These environmental parameters may serve as
# covariates regarding the mortality or movement of 
# individuals. The environment will be describe through a
# series of masks.
###########################################################

###########################################################
# HABITAT PREFERABILITY:
# Habitat preferability will have an effect on the movement 
# of juveniles as well as the natural mortality of all life
# stages. This habitat preferability is independent of any
# anthropogenic effects otherwise included in the model.
# Habitat preferability is also assumed independent of pre-
# recruit life stage (i.e. is the same for all individuals)
###########################################################

###########################################################
# ANTHROPOGENIC EFFCTS:
# Anthropogenic effects will add special mortality 
# covariates to the individuals occupying the areas in
# which they occur.
###########################################################

###########################################################
# CURRENT:
# Will provide information on the approximate movement of 
# larvae and juveniles due to water currents. 
###########################################################

###########################################################
# SPAWNING GROUNDS:
# Will provide indication of spawning grounds. 
###########################################################